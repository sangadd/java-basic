package class1;

public class Student {
    String name;
    int age;
    int grade;

    /*
    * 클래스에 정의한 변수들을 멤버 변수 또는 필드라고 한다.
    * 멤버 변수 (Member Variable) : 이 변수들은 특정 클래스에 소속된 멤버이기 때문에 이렇게 부른다.
    * 필드 (Field) : 데이터 항목을 가르키는 전통적인 용어이다. 데이터베이스, 엑셀 등에서 데이터 각각의 항목을 필드라고 한다.
    * 자바에서 멤버 변수, 필드는 같은 뜻이다.
    * 클래스에 소속된 변수를 뜻한다.
    *
    * 클래스는 관례상 대문자로 시작하고 낙타 표기법을 사용한다. (낙타 표기법은 카멜 케이스라고도 부르는데 단어 전체적으로 소문자를 사용하지만
    * 맨 첫 글자를 제외한 각 합성어의 첫 글자만 대문자로 표기한다.)
    */
}
